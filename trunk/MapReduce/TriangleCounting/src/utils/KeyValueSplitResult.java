public class KeyValueSplitResult {
    public String Key;
    public String Value;
    
    public KeyValueSplitResult()
    {
        
    }
    
    public KeyValueSplitResult(String newKey, String newValue)
    {
        Key = newKey;
        Value = newValue;
    }
}
